

public class MathExamples {
    public static void main(String[] args) {
        double result;
        result = Math.pow(2, 3);
        result = Math.abs(-5);
        result = Math.sqrt(16);
        result = Math.ceil(3.14);
        result = Math.floor(3.99);
        result = Math.max(10,20);
        System.out.println(result);
    }
}
