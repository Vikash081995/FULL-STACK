public class Hello {
	public static void main(String[] args) {
       int num1=9;
       int num2=10;
       byte by = 127;
       long l = 1000000000000000000L;
       float f = 1.234f;
       double d=1.23456789;
       char c = 'A';
       boolean b = true;
       String s = "Hello World";
       System.out.println(num1); //prints 9
       System.out.println(num2); //prints 10
       System.out.println(by); //prints 127
       System.out.println(l); //prints 1000000000000000000
       System.out.println(f); //prints 1.234
       System.out.println(d); //prints 1.23456789
       System.out.println(c); //prints A
       System.out.println(b);   //prints true
       System.out.println(s); //prints Hello World
	}
}