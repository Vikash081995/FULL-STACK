//to trim whitespace from the beginning and end of a string, use the trim() method
let str = "          hello world          ";        
console.log(str.trim()); // hello world
// 
//to trim whitespace from the beginning of a string, use the trimStart() method
let str1 = "          hello world          ";    
console.log(str1.trimStart()); // hello world     

//to trim whitespace from the end of a string, use the trimEnd() method
let str2 = "          hello world          ";    
console.log(str2.trimEnd()); //          hello world